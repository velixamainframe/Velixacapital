-- ============================================================
-- Row Level Security
-- ------------------------------------------------------------
-- The app talks to this project using the SERVICE ROLE key from
-- server-side route handlers, which bypasses RLS. We still enable
-- RLS on every table as defence-in-depth — the anon key (which is
-- the only key that can reach the browser) is denied by default.
-- ============================================================

alter table public.users                 enable row level security;
alter table public.employees             enable row level security;
alter table public.partner_profiles      enable row level security;
alter table public.leads                 enable row level security;
alter table public.affiliate_links       enable row level security;
alter table public.partner_applications  enable row level security;
alter table public.call_sheets           enable row level security;
alter table public.call_assignments      enable row level security;
alter table public.call_outcomes         enable row level security;
alter table public.follow_ups            enable row level security;
alter table public.partner_files         enable row level security;
alter table public.blogs                 enable row level security;
alter table public.credit_cards          enable row level security;
alter table public.careers               enable row level security;
alter table public.audit_logs            enable row level security;

-- Public-read policies for published content (blogs, careers, cards).
-- These let the marketing site render without auth.
create policy "blogs_public_read" on public.blogs
  for select to anon, authenticated using (published = true);

create policy "careers_public_read" on public.careers
  for select to anon, authenticated using (published = true);

create policy "credit_cards_public_read" on public.credit_cards
  for select to anon, authenticated using (published = true);

create policy "affiliate_links_public_read" on public.affiliate_links
  for select to anon, authenticated using (true);

