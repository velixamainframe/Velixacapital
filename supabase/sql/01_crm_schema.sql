-- ============================================================
-- updated_at trigger (auto-bump on every update)
-- ============================================================
create or replace function public.bump_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end; $$;

do $$
declare t text;
begin
  for t in select unnest(array[
    'users','employees','partner_profiles','partner_files',
    'blogs','credit_cards','careers'
  ])
  loop
    execute format('drop trigger if exists %I on public.%I;', t || '_bump_updated_at', t);
    execute format('create trigger %I before update on public.%I for each row execute function public.bump_updated_at();', t || '_bump_updated_at', t);
  end loop;
end $$;

