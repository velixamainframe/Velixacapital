create index if not exists assignments_employee_idx on public.call_assignments (employee_id);
create index if not exists assignments_status_idx on public.call_assignments (status);
create index if not exists assignments_sheet_idx on public.call_assignments (call_sheet_id);

-- ---------- call outcomes (one per logged call) ----------
