-- ============================================================
-- Velixa Capital — CRM Supabase schema
-- ------------------------------------------------------------
-- Run this ENTIRE script in the SQL Editor of your CRM Supabase project.
--
-- This project stores the application database:
--   users, employees, partners, leads, call assignments, call outcomes,
--   follow-ups, partner files, blogs, careers, credit cards,
--   affiliate links, audit logs.
--
-- PII columns (name, mobile, email, pan) are written AES-256-GCM
-- ENCRYPTED by the application layer (src/lib/crypto.ts). The raw
-- values in these columns are never readable from the DB directly —
-- even a SQL dump exposes no plaintext customer data.
--
-- NOTE: This schema mirrors the Prisma schema used for local dev
-- (prisma/schema.prisma). When you change one, update the other.
-- ============================================================

-- ---------- enums ----------
do $$ begin
  create type public.app_role as enum ('admin', 'user', 'partner', 'employee');
exception when duplicate_object then null; end $$;

do $$ begin
  create type public.lead_status as enum ('new', 'contacted', 'qualified', 'converted', 'lost');
exception when duplicate_object then null; end $$;

do $$ begin
  create type public.assignment_status as enum (
    'available', 'in_progress', 'success', 'rejected',
    'not_connected', 'reschedule', 'callback', 'converted', 'dnd'
  );
exception when duplicate_object then null; end $$;

-- ---------- users (application-side profiles; auth lives in the AUTH project) ----------
-- The `id` column here is a cuid generated by the app. It does NOT need to
-- match auth.users.id in the AUTH project — the link is by email.
create table if not exists public.users (
  id text primary key,
  email text unique not null,
  password_hash text,
  display_name text,
  role public.app_role not null default 'user',
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create index if not exists users_email_idx on public.users (email);

-- ---------- employees ----------
create table if not exists public.employees (
  id text primary key,
  user_id text unique not null references public.users(id) on delete cascade,
  full_name text not null,            -- encrypted
  mobile text not null,               -- encrypted
  email text not null,                -- encrypted
  employee_code text,
  designation text,
  target_calls integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create index if not exists employees_is_active_idx on public.employees (is_active);

-- ---------- partner profiles ----------
create table if not exists public.partner_profiles (
  id text primary key,
  user_id text unique not null references public.users(id) on delete cascade,
  partner_code text,
  company_name text,
  mobile text,                        -- encrypted
  city text,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- ---------- leads (public site form submissions) ----------
