-- ---------- call assignments (a lead routed to an employee) ----------
create table if not exists public.call_assignments (
  id text primary key,
  call_sheet_id text references public.call_sheets(id) on delete set null,
  employee_id text references public.employees(id) on delete set null,  -- null = available in universal pool
  customer_name text not null,        -- encrypted
  customer_mobile text not null,      -- encrypted
  customer_email text,                -- encrypted
  city text,
  service text,
  loan_amount numeric,
  employment_type text,
  monthly_income numeric,
  notes text,
  status text not null default 'available',
